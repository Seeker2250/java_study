package chap02;

public class Pyramid02 {
    public static void main(String[] args) {
        //control + / : coment 처리
//        control + z : undo
//        int i, j;
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
//        = : assignment operator
//        == : comparison operator
    }
}
